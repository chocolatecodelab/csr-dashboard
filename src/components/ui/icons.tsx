// Function untuk mapping string name ke icon component untuk DataTable actions
export const getActionIcon = (iconName: string) => {
  const iconMap: Record<string, React.ComponentType<PropsType>> = {
    eye: Eye,
    edit: Edit,
    trash: Trash,
    delete: Trash, // alias
    view: Eye, // alias
    update: Edit, // alias
    plus: Plus,
    add: Plus, // alias
    download: Download,
    settings: Settings,
    check: Check,
    close: X,
    x: X,
  };

  return iconMap[iconName.toLowerCase()] || Eye; // fallback ke Eye icon
};
